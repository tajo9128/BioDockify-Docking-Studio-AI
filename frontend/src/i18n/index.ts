export function t(locale: Locale, key: string): string {
  return translations[locale]?.[key] || translations.en[key] || key
}

export function getLocale(): Locale {
  const saved = localStorage.getItem('locale')
  if (saved && ['en', 'es', 'zh', 'ar'].includes(saved)) return saved as Locale
  const browser = navigator.language.slice(0, 2)
  if (['es', 'zh', 'ar'].includes(browser)) return browser as Locale
  return 'en'
}

export function setLocale(locale: Locale) {
  localStorage.setItem('locale', locale)
  document.documentElement.lang = locale
  document.documentElement.dir = translations[locale] ? 'rtl' : 'ltr'
  const loc = locales.find(l => l.code === locale)
  document.documentElement.dir = loc?.dir || 'ltr'
}
